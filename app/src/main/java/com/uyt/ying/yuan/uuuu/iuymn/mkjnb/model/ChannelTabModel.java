package com.uyt.ying.yuan.uuuu.iuymn.mkjnb.model;

public class ChannelTabModel {
    String title;
    long id;

    public ChannelTabModel(String title, long id) {
        this.title = title;
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }
}
